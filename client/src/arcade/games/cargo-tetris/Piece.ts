export function getKicks(
  type: PieceType,
  fromRot: number,
  toRot: number,
): number[][] {
  const key = `${fromRot}>${toRot}`;
  return type === "I"
    ? KICKS_I[key] || [[0, 0]]
    : KICKS_NORMAL[key] || [[0, 0]];
}

export class Piece {
  type: PieceType;
  rotation: number;
  col: number;
  row: number;

  constructor(type: PieceType, col: number, row: number, rotation = 0) {
    this.type = type;
    this.col = col;
    this.row = row;
    this.rotation = rotation;
  }

  cells(): number[][] {
    return PIECE_SHAPES[this.type][this.rotation].map(([r, c]) => [
      this.row + r,
      this.col + c,
    ]);
  }

  clone(): Piece {
    return new Piece(this.type, this.col, this.row, this.rotation);
  }
}
